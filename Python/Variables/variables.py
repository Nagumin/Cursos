# Variables en Python

# Declaración e inicialización de variables
edad = 21
altura = 1.75
pais = 'Colombia'

# Acceder a las variables
print("Valores iniciales")
print('Edad:',edad)
print('Altura:',altura)
print('País:',pais)

# Modificar el valor de una variable
edad = 30
altura = 1.80
pais = 'España'

# Acceder a las variables
print("\nValores modificados")
print('Edad:',edad)
print('Altura:',altura)
print('País:',pais)

# En python el tipo es dinámico
edad = 'treinta'
print('Edad:',edad)

#Si queremos acceder a una variable no declarada manda error
telefono = '3554451112'
print('Teléfono:',telefono)


